#Tuple is an immutable data type in python. 
#It is also iteralable ; sequence of data; enclosed within small brackets.

#creating non-empty tuples
a = (1,2,3)
a = (1, "a",[1,2], "b")

#tuple() is a built-in function to define / create a tuple

a = tuple([1,2,3])  #Type casting
print(a)  #(1,2,3)

# creating empty tuple

a = ()
print(a)
b = tuple()
